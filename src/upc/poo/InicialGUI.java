    private void recuperarSesion() throws FileNotFoundException{
        
        int reply = JOptionPane.showConfirmDialog(null, "Vols recuperar les dades de la sessi√≥ anterior?", "", JOptionPane.YES_NO_OPTION);
        if(reply == JOptionPane.YES_OPTION){
            //Teclado.setComandosIniciales(true);
            controlador = Main.getControlador();
            iu = Main.getIu();
        }
        else {
            //Teclado.setComandosIniciales(false);
            controlador = new Controlador();
            iu = new InterfazUsuario(controlador);
            PrintWriter out = new PrintWriter("ComandosGuardados.txt");
            out.flush();
            out.close();
        }
    }

